/**
 * Package Builder
 *
 * Builds exportable test packages with proper project structure.
 */

import type { TalosApplication, TalosTest } from "../types.js";
import type { TalosRepository } from "../repository.js";
import type { ExportConfig } from "../config.js";
import { CredentialSanitizer, type SanitizationResult } from "./credential-sanitizer.js";

// ── Types ─────────────────────────────────────────────────────────────────────

export type PackageBuilderOptions = {
  config: ExportConfig;
  repository: TalosRepository;
};

export type PackageTemplate = "playwright" | "minimal" | "full";

export type PackageContents = {
  files: PackageFile[];
  totalSize: number;
  testCount: number;
};

export type PackageFile = {
  path: string;
  content: string;
  type: "test" | "config" | "fixture" | "page-object" | "utility" | "doc";
};

export type BuildOptions = {
  template?: PackageTemplate;
  includePageObjects?: boolean;
  includeFixtures?: boolean;
  sanitizeCredentials?: boolean;
  generateReadme?: boolean;
  generateEnvTemplate?: boolean;
};

